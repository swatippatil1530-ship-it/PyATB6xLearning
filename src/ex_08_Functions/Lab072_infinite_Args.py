
def print_mult_arg(*args):
    #args-list
    for i in args:
        print(i)
#it will print value each line like new value printng this is the use of *args
print_mult_arg("swati")
print_mult_arg(2,3,8,6,45,9,3)
print_mult_arg("swati",2,3.5)
print_mult_arg("swati","shreya",5,True)
print_mult_arg("swati","shreya",5,True)